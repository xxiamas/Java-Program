//Program to using switch case
public class Switch {
    public static void main(String args[]){
        int num1=1;
        switch(num1){
            case 1:
            System.out.println("1");
            break;
            case 2:
            System.out.println("2");
            break;
            case 3:
            System.out.println("3");
            break;
            default:
            System.out.println("Error");
        }
    }
    
}
